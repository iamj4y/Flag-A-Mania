function countryPhrasePage() {
    const contentDiv = document.getElementById("phraseSection");
    const phraseList = document.getElementById("phraseList");
    const navLinks = document.querySelectorAll('#phrasesToggle a');

    let countryAbbr = '';
    const cookies = document.cookie.split(';');
    for (const cookie of cookies) {
        const trimmed = cookie.trim();
        if (trimmed.startsWith("countrySPSelected=")) {
            countryAbbr = trimmed.split('=').pop();
        }
    }

    let countryName = "";


    const countryObj = countrySet.find(c => c.country === countryAbbr);
    if (countryObj) {
        countryName = countryObj.name;
        document.getElementById("countryName").textContent = countryName;

        const flagImg = document.getElementById("flagImg");
        const img = document.createElement("img");
        img.src = `../img/flag/${countryAbbr}.svg`;
        img.alt = "flag";
        img.width = 200;
        flagImg.appendChild(img);
    }

    navLinks.forEach(link => {
        link.addEventListener("click", function (e) {
            e.preventDefault();
    
            const categoryId = this.id;
            const categoryText = categoryMap[categoryId];
            phraseList.innerHTML = "";
    
            if (
                phraseData[categoryText] &&
                phraseData[categoryText][countryName]
            ) {
                const phrases = phraseData[categoryText][countryName];
    
                phrases.forEach(phrase => {
                    const li = document.createElement("li");
                    li.setAttribute('id', 'originalPhrase')
                    li.innerHTML = `
                        <strong> ${phrase.original}</strong><br>
                        <ul><li id="translatedPhrase">
                        <i>${phrase.translated}</i><br></li></ul>
                    `;
                    phraseList.appendChild(li);
                });
            } else {
                phraseList.innerHTML = "<li>No phrases found for this category/country.</li>";
            }
        });
    });
    
}


window.addEventListener("load", countryPhrasePage, false);
